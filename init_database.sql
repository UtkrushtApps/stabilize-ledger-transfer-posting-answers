CREATE EXTENSION IF NOT EXISTS pg_stat_statements;

DROP TABLE IF EXISTS worker_heartbeats CASCADE;
DROP TABLE IF EXISTS ledger_audit_events CASCADE;
DROP TABLE IF EXISTS ledger_entries CASCADE;
DROP TABLE IF EXISTS transfer_requests CASCADE;
DROP TABLE IF EXISTS account_balances CASCADE;
DROP TABLE IF EXISTS accounts CASCADE;
DROP TABLE IF EXISTS tenants CASCADE;

CREATE TABLE tenants (
  tenant_id integer PRIMARY KEY,
  tenant_name text NOT NULL,
  risk_tier text NOT NULL CHECK (risk_tier IN ('standard','enhanced','restricted')),
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE accounts (
  account_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL REFERENCES tenants(tenant_id),
  external_account_ref text NOT NULL,
  account_type text NOT NULL CHECK (account_type IN ('checking','savings','wallet','merchant_settlement')),
  currency char(3) NOT NULL DEFAULT 'USD',
  status text NOT NULL CHECK (status IN ('active','frozen','closed')),
  opened_at timestamptz NOT NULL,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  UNIQUE (tenant_id, external_account_ref)
);

CREATE TABLE account_balances (
  account_id bigint PRIMARY KEY REFERENCES accounts(account_id),
  tenant_id integer NOT NULL REFERENCES tenants(tenant_id),
  currency char(3) NOT NULL DEFAULT 'USD',
  available_balance numeric(18,2) NOT NULL,
  pending_balance numeric(18,2) NOT NULL DEFAULT 0,
  updated_at timestamptz NOT NULL DEFAULT now(),
  CHECK (available_balance > -1000000),
  CHECK (pending_balance > -1000000)
);

CREATE TABLE transfer_requests (
  transfer_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL REFERENCES tenants(tenant_id),
  from_account_id bigint NOT NULL REFERENCES accounts(account_id),
  to_account_id bigint NOT NULL REFERENCES accounts(account_id),
  amount numeric(18,2) NOT NULL CHECK (amount > 0),
  currency char(3) NOT NULL DEFAULT 'USD',
  status text NOT NULL CHECK (status IN ('pending','posted','failed','cancelled')),
  idempotency_key text NOT NULL UNIQUE,
  requested_at timestamptz NOT NULL DEFAULT now(),
  posted_at timestamptz,
  failure_reason text,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  CHECK (from_account_id <> to_account_id)
);

CREATE TABLE ledger_entries (
  ledger_entry_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL REFERENCES tenants(tenant_id),
  transfer_id bigint REFERENCES transfer_requests(transfer_id),
  account_id bigint NOT NULL REFERENCES accounts(account_id),
  posted_at timestamptz NOT NULL,
  direction text NOT NULL CHECK (direction IN ('debit','credit')),
  amount numeric(18,2) NOT NULL CHECK (amount > 0),
  currency char(3) NOT NULL DEFAULT 'USD',
  status text NOT NULL CHECK (status IN ('posted','reversed','void')),
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb
);

CREATE TABLE ledger_audit_events (
  audit_event_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL REFERENCES tenants(tenant_id),
  transfer_id bigint REFERENCES transfer_requests(transfer_id),
  event_type text NOT NULL,
  event_at timestamptz NOT NULL DEFAULT now(),
  actor text NOT NULL,
  details jsonb NOT NULL DEFAULT '{}'::jsonb
);

CREATE TABLE worker_heartbeats (
  worker_name text PRIMARY KEY,
  tenant_id integer REFERENCES tenants(tenant_id),
  last_seen_at timestamptz NOT NULL,
  current_transfer_id bigint,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb
);

