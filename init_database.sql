-- A posted transfer must have at most one debit and at most one credit ledger row.
-- The posting function below inserts both rows atomically in the same transaction, so
-- these indexes prevent duplicate ledger legs during retry/concurrency scenarios while
-- still allowing non-transfer historical ledger rows with transfer_id IS NULL.
CREATE UNIQUE INDEX ledger_entries_one_debit_per_transfer_idx
  ON ledger_entries(transfer_id)
  WHERE transfer_id IS NOT NULL AND direction = 'debit';

CREATE UNIQUE INDEX ledger_entries_one_credit_per_transfer_idx
  ON ledger_entries(transfer_id)
  WHERE transfer_id IS NOT NULL AND direction = 'credit';

