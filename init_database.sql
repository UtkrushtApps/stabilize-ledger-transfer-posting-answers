CREATE OR REPLACE FUNCTION process_transfer(p_transfer_id bigint)
RETURNS TABLE(processed_transfer_id bigint, debit_account_id bigint, credit_account_id bigint, processed_amount numeric, final_status text)
LANGUAGE plpgsql
AS $$
DECLARE
  r transfer_requests%ROWTYPE;
  v_locked_balance_count integer;
  v_updated_balance_count integer;
  v_inserted_ledger_count integer;
  v_posted_at timestamptz;
BEGIN
  SELECT *
  INTO r
  FROM transfer_requests
  WHERE transfer_id = p_transfer_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'transfer % not found', p_transfer_id;
  END IF;

  IF r.status = 'posted' THEN
    RETURN QUERY SELECT r.transfer_id, r.from_account_id, r.to_account_id, r.amount, r.status;
    RETURN;
  END IF;

  IF r.status <> 'pending' THEN
    RAISE EXCEPTION 'transfer % is not pending', p_transfer_id;
  END IF;

  /*
    The previous workflow updated the debit balance first and the credit balance
    second. Two opposite-direction transfers for the same account pair could
    therefore acquire row locks in opposite orders and deadlock:

      transfer A: lock account 1, then wait for account 2
      transfer B: lock account 2, then wait for account 1

    Lock every balance row touched by the transfer up front, in one deterministic
    account_id order, before applying either delta. Transactions that conflict on
    the same accounts now queue behind the first account in the ordered set rather
    than forming a wait cycle. This preserves the same debit/credit semantics while
    making concurrent opposite-direction posting safe.
  */
  SELECT count(*)
  INTO v_locked_balance_count
  FROM (
    SELECT b.account_id
    FROM account_balances b
    WHERE b.tenant_id = r.tenant_id
      AND b.account_id IN (r.from_account_id, r.to_account_id)
    ORDER BY b.account_id
    FOR UPDATE
  ) AS locked_balances;

  IF v_locked_balance_count <> 2 THEN
    RAISE EXCEPTION 'expected to lock 2 balance rows for transfer %, locked %', r.transfer_id, v_locked_balance_count;
  END IF;

  v_posted_at := clock_timestamp();

  UPDATE account_balances b
  SET available_balance = b.available_balance +
        CASE
          WHEN b.account_id = r.from_account_id THEN -r.amount
          WHEN b.account_id = r.to_account_id THEN r.amount
          ELSE 0
        END,
      updated_at = v_posted_at
  WHERE b.tenant_id = r.tenant_id
    AND b.account_id IN (r.from_account_id, r.to_account_id);

  GET DIAGNOSTICS v_updated_balance_count = ROW_COUNT;
  IF v_updated_balance_count <> 2 THEN
    RAISE EXCEPTION 'expected to update 2 balance rows for transfer %, updated %', r.transfer_id, v_updated_balance_count;
  END IF;

  INSERT INTO ledger_entries (tenant_id, transfer_id, account_id, posted_at, direction, amount, currency, status, metadata)
  VALUES
    (r.tenant_id, r.transfer_id, r.from_account_id, v_posted_at, 'debit', r.amount, r.currency, 'posted', jsonb_build_object('source','posting_worker')),
    (r.tenant_id, r.transfer_id, r.to_account_id, v_posted_at, 'credit', r.amount, r.currency, 'posted', jsonb_build_object('source','posting_worker'));

  GET DIAGNOSTICS v_inserted_ledger_count = ROW_COUNT;
  IF v_inserted_ledger_count <> 2 THEN
    RAISE EXCEPTION 'expected to insert 2 ledger rows for transfer %, inserted %', r.transfer_id, v_inserted_ledger_count;
  END IF;

  INSERT INTO ledger_audit_events (tenant_id, transfer_id, event_type, actor, details)
  VALUES (r.tenant_id, r.transfer_id, 'posted', current_user, jsonb_build_object('from_account_id', r.from_account_id, 'to_account_id', r.to_account_id));

  UPDATE transfer_requests
  SET status = 'posted',
      posted_at = v_posted_at,
      failure_reason = NULL
  WHERE transfer_id = r.transfer_id;

  RETURN QUERY SELECT r.transfer_id, r.from_account_id, r.to_account_id, r.amount, 'posted'::text;
END;
$$;

CREATE OR REPLACE FUNCTION reset_deadlock_demo()
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
